          Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SESEAY), convocaron a
          todas las Instituciones Educativas de nivel superior, públicas y privadas del estado,
          al 1er Concurso de Oratoria, el cual se llevó a cabo el día de ayer 12 de marzo del
          año en curso a las 9 horas en el Auditorio Manuel Cepeda Peraza del edificio central
          de la Universidad Autónoma de Yucatán (UADY). Contamos con la participación de
          15 instituciones educativas con un total de 35 de 40 alumnos inscritos de Mérida,
          Progreso y Valladolid. Los temas de la exposición versan sobre corrupción, en
          cualquier modalidad que el participante quiera exponer. La finalidad del concurso es
          crear cultura entre los jóvenes en materia de anticorrupción, buscando con esto
          conocer la percepción que tienen los estudiantes de un tema que nos ocupa a toda
          la sociedad. 
				</p>
