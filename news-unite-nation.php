					“Red mexicana del Pacto Mundial de las Naciones Unidas y su quehacer a favor de la integridad de empresas y gobierno”
					Este jueves 6 de junio recibimos la visita en la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán del maestro Emilio Guerra Díaz, Director de United Nations Global Compact Network México, quien realiza el primer contacto con el Sistema Estatal Anticorrupción y nos platica acerca de las actividades que han estado desarrollando en cuanto al Pacto Global de las Naciones Unidas México, pacto mundial de valores que tiene como objetivo más allá de una responsabilidad social corporativa, dirige esfuerzos al desarrollo sustentable.
					En cuanto a ello, se encuentra la red mexicana del Pacto Mundial de las Naciones Unidas y su quehacer a favor de la integridad de empresas y gobierno, realizando hasta la fecha 14,500 enlaces con empresas comprometidas, de ellas hay 74 redes locales, 3 mil no empresariales; en general hay una participación en este número global del 70% de empresas, 2% universidades y 3% gobierno. Esto determina líneas de acción y proyectos alineados a ciertos estándares laborales con enfoque en diversas áreas, obviamente tomando en cuenta temáticas relacionadas con “Anticorrupción”.
					La prioridad de los enlaces y vinculación de la organización consiste en los siguientes aspectos:
				</p>
				<ol class="w-80 paragraph-md text-blue_1 p-2" type="1">
					<li>Sensibilización</li>
