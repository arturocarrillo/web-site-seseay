				<h2 class="title text-blue_1">
					“Primer Encuentro municipal Anticorrupción”</h2>
			</div>
			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
					En el Marco del Primer Encuentro Municipal Anticorrupción, el Sistema Estatal Anticorrupción en coordinación con el Sistema Nacional Anticorrupción y con apoyo del Instituto de Desarrollo Regional y Municipal (INDERM) del Gobierno del Estado se llevó a cabo el <span>“Primer Encuentro municipal Anticorrupción”</span> que fue inaugurado el  día miércoles 10 de julio en punto de las 8:30 a.m. teniendo como sede el auditorio “Víctor Manuel Cervera Pacheco” ubicado en el  recinto del Tribunal Superior de Justicia del Estado; como parte del presídium asistió la M.F. Lizbeth Beatriz Basto Avilés, en representación del Gobernador, Mauricio Vila Dosal, el Lic. José Octavio López Presa, Presidente del Sistema Nacional Anticorrupción, la Lic. Graciela A. Torres Garma, Presidenta del Sistema Estatal Anticorrupción de Yucatán, la C. Diana Canto Moreno, Regidora síndico Municipal  en representación del Presidente Municipal de Mérida , el Abogado Ricardo de Jesús Ávila Heredia, Presidente del Tribunal Superior de Justicia y del Consejo de la Judicatura del Poder Judicial del Estado de Yucatán.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Una vez realizada la ceremonia protocolaria se dió paso a la jornada, que consistió en la presentación de una mesa panel que abordo diversos temas:   
				</p>
				<ol type="I" class="w-90 paragraph-md text-blue_1 p-2">
					<li>Sistema Estatal Anticorrupción.</li>
					<li>Código de ética.</li>	
					<li>Órgano de control interno (municipio de Mérida).</li>	
					<li>Herramientas jurídicas de control a la corrupción y responsabilidad administrativa.</li>	
					<li>Subcomité de Fiscalización.</li>	
				</ol>
				<p class="paragraph-md text-blue_1 p-2">
					Los ponentes provinieron de distintas dependencias, por parte del H. Ayuntamiento de Mérida participaron la Lic. Martha Elena Gómez Nechar y el C.P. Juan José Patrón Martínez, en representación del Comité de Participación Ciudadana (CPC) estuvo presente la Lic. Mónica Febles Álvarez-Icaza  y por parte de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SESEAY) participaron la Lic. Landy Mendoza Seba, Lic. César D. Martínez Paredes, y el Secretario Técnico de la misma, el Lic. Edwin M. Rejón Pacheco.  
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					<span class="bold">Al interior del Estado</span><br>
					Conscientes de la importancia de replicar esta información al interior del Estado, la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SESEAY) y el Instituto de Desarrollo Regional y Municipal (INDERM)   realizaron eventos  en Izamal, Tekax y Valladolid, replicando la mesa panel impartida en Mérida, pero ahora teniendo como sede la Universidad Tecnológica del Centro (UTC), Universidad tecnológica del Sur (UTS) y la Universidad de Oriente en Valladolid (UNO), en dichos encuentros participaron los presidentes y presidentas municipales, secretarios, síndicos y síndicas 
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Tras la exposición de los temas, en cada sede hubo espacio para que los asistentes plantearan a los panelistas preguntas concretas en los temas, generando una dinámica de participación e involucramiento de los representantes de los H. Ayuntamientos con la Secretaría Ejecutiva Estatal Anticorrupción. 
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					En total acudieron al llamado 90 municipios del estado en las diferentes sedes, con los cuales logramos identificar a través de un diagnóstico las necesidades que presentan en cada municipio en materia de control interno e integración de subcomité de fiscalización, lo anterior para que el diseño de las herramientas necesarias para la integración e implementación en los ayuntamientos de los órganos de control interno y los subcomités de fiscalización. 
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					<span class="bold">Otras actividades</span><br>
					Como parte de la jornada del <span>“Primer Encuentro municipal Anticorrupción”</span>, el Comité de Participación Ciudadana (CPC) en coordinación con el Sistema Nacional Anticorrupción (SNA) a través de su presidente el Lic José Octavio López Presa, el día miércoles 10 de julio en punto de las 19:00 horas en el auditorio “Hermano Pablo Hernández García”  recinto ubicado en la Universidad  Marista de Mérida, se desarrolló la Conferencia Magistral: “La Anticorrupción desde la Educación” evento que convocó a instituciones de educación superior privadas y públicas del estado, así como al personal de la Secretaría de Educación del Gobierno del Estado de Yucatán (SEGEY).
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Así como también se tuvo un encuentro con las cámaras empresariales, el día 12 de julio a través de la Conferencia Magistral: “El Sistema Nacional Anticorrupción de Yucatán”, realizada a las 18:00 horas en la “Sala Ejecutiva” del Instituto Tecnológico de Mérida. 
				</p>
