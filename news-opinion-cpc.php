			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
					“Impunidad a exediles yucatecos” señala la edición del sábado 20 de julio de 2019 del Diario de Yucatán en la portada de su edición impresa de ese día. Notas periodísticas y entrevistas sobre este tema de impunidad se han publicado los días 21, 22, 23 y 24 del presente en DY.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					El Comité de Participación Ciudadana (CPC) del Sistema Estatal Anticorrupción del estado de Yucatán (SEAY) expresa su opinión sobre esta nota periodística:
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					1)- El grado de corrupción e impunidad que amplio sector de la ciudadanía veía en acciones de gobierno hizo que a principios de esta década, cámaras empresariales, organizaciones civiles, académicos, líderes de opinión, medios de comunicación y muchas voces más, pidieran poner un “hasta aquí” a esa situación.
					Eso fue el origen de lo que hoy son los Sistemas Anticorrupción que basados en una nueva Ley daban un nuevo frente de combate a las faltas administrativas, actos de corrupción e impunidad.

				</p>
				<p class="paragraph-md text-blue_1 p-2">
					2)- La impunidad existente, que señalan las notas periodísticas de DY, son argumentos contundentes para que la ciudadanía tenga, desde el 19 de julio de 2017, un Sistema Estatal Anticorrupción que preside, por ley, un CIUDADANO. Algo inédito en la legislación mexicana y de nuestro estado. No existe un Sistema similar.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					3)- Lo señalado por DY sobre las prescripciones de expedientes que denuncias desvíos en las cuentas públicas en periodos anteriores al 2017 (que no existía el SEAY) y que pueden significar posibles actos de corrupción e impunidad, significan para toda la sociedad la oportunidad de medir, de comparar, de acompañar de cerca, si a partir de la existencia del SEAY, la situación va a seguir igual, peor o mejor. Para ver si el SEAY, presidido por un ciudadado, funciona, no funciona o qué le falta para que funcione como espera la Ciudadanía.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					4)- Para que el SEAY naciera se modificaron casi una decena de leyes y decretos. Y en el CPC pensamos que estas modificaciones deben de seguir: <br>
     				4.1- Apoyamos la iniciativa de tener una Fiscalía Anticorrupción autónoma e independiente. Le pedimos al Congreso que le de celeridad a esta iniciativa que envió el Ejecutivo Estatal.
					4.2- Invitamos nuevamente al ciudadano que use el Mecanismo de Queja, herramienta creada en el seno del SEAY, como forma de responder a un posible acto de corrupción que lo afecte. Y toda nuestra intención es que esas quejas no terminen en expedientes que prescriban. <br>
           			4.3- Solicitamos a los municipios del estado, como lo hicimos en el Encuentro que tuvimos con 91 municipios en días pasados, que fortalezcan sus medidas de control interno y fiscalización para no caer en posibles faltas administrativas que puedan tipificarse como posibles actos de corrupción. La consigna ciudadana sigue siendo muy clara: si hay corrupción debe de haber consecuencias para quien resulte responsable. <br>
					4.4- Exhortamos a los partidos políticos, a que eviten postular para cargos de elección popular a personas que tienen alguna denuncia no aclarada por desvíos en el manejo de recursos públicos. Un candidato debe de entrar a una contienda electoral con hoja en blanco en este tema e imperar el “si nada debes, nada temes”

				</p>
				<p class="paragraph-md text-blue_1 p-2">
					5)- Para concluir deseamos compartir una opinión que a través de nuestra página de facebook publicamos el 19 de julio de 2019, un día antes de los artículos de DY, donde expresamos nuestro deseo de sinergia y empatía con nuestros pares, los ciudadanos:
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					“Hoy, 19 de julio de 2019, se cumplen dos años de que entró en vigor la Ley del Sistema Estatal Anticorrupción de Yucatán. Con su publicación en el Diario Oficial, empezó a gestarse todo un Sistema cuya finalidad última es el combate a la corrupción.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Aprovechamos esta efeméride para enfatizar la importancia que la información y la comunicación tienen en este inédito esquema de Sistema Anticorrupción. Un esquema totalmente diferente a todos los demás Sistemas de cualquier índole en el país y en nuestro estado.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					El Comité de Participación Ciudadana (CPC) del Sistema Estatal Anticorrupción de Yucatán (SEAY), es un ente totalmente ciudadano. Por ley, el presidente del CPC es el presidente del SEAY.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Invitamos a todos los medios de comunicación a fortalecer el vínculo informativo con el CPC para darle al ciudadano información sobre lo que es, sus atribuciones, sus logros, sus dificultades, tropiezos, proyectos, acciones y pasos que se están dando, desde el propio SEAY, para prevenir y combatir faltas administrativas y actos de corrupción.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Son los Medios nuestro enlace principal con el ciudadano y si los Medios nos abren espacios para hablar del SEAY, el ciudadano tendrá información de fuente ciudadana para receptor ciudadano.
					El CPC no es un órgano o dependencia de gobierno sino una instancia totalmente ciudadana que por Ley, está en un Sistema que involucra a dependencias del gobierno.

				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Esta es la gran aportación, según nuestra visión, del Sistema Anticorrupción: un ciudadano encabeza el Sistema, se sienta a la mesa con autoridades estatales de primer nivel y permite incluir, en la planeación de políticas públicas que puedan abatir la corrupción, la visión del ciudadano.
					A todos los medios de comunicación: somos fuente informativa ciudadana y los invitamos a que nos ayuden a mantener informados a sus públicos en un tema inédito, complejo de explicar y entender, pero de suma importancia para que, cada vez más, la participación ciudadana se vea reflejada en la forma que deseamos sean nuestros gobiernos.

				</p>
