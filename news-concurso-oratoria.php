            Veytia Flores conocido docente, conferencista y locutor de radio, así como el Lic.
            Gaspar Humberto Gómez Chacón quien ha sido catedrático en
            diferentes instituciones y actualmente dirige la Compañía Editorial de la
            Península.
				</p>
