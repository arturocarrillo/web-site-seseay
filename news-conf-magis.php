				<p class="paragraph-md text-blue_1 p-2">
					 <cite>Mtro. Antonio Molpeceres, Coordinador Residente de la ONU en México</cite>

					En el marco de la Asamblea Nacional organizada por la que tenía como tópico el “Impacto del desarrollo sostenible como eje transversal del perfil profesional en las facultades y escuelas de negocios”, Antonio Molpeceres, Coordinador Residente de la ONU en México impartió una conferencia magistral alrededor de las 10:00 a.m. en el Hotel Fiesta Americana, dicha plática comenzó con los Objetivos del Desarrollo del Milenio, producto de una primera cooperación internacional entre los jefes de estado, los avances producto de los ODM incentivaron a los líderes a una nueva agenda de trabajo que derivó en los Objetivos del Desarrollo Sostenible, una colección de diecisiete objetivos apostaban ahora por el tema ambiental, la vida marina, la reducción de la pobreza, la equidad de género y por instituciones de paz y justicia.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					A dicha conferencia asistió la Lic. Graciela Torres Garma en compañía del Lic. Edwin Manuel Rejón Pacheco, presidenta del Comité de Participación Ciudadana y Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción, respectivamente. Ambos tuvieron la oportunidad de platicar momentos antes de su conferencia con el Sr. Molpeceres, quien les comentó, que la reducción y erradicación era todo un reto pero que su desaparición sin duda era necesaria, Molpeceres también reconoció la importancia del Sistema Estatal Anticorrupción de Yucatán.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Durante la conferencia Antonio Molpeceres llamó a la cooperación intersectorial, entre gobierno, ciudadanía y empresarios, pues esa sinergia es la única vía para llegar a la mejora del mundo en el que vivimos, y por el que vale la pena luchar por ser el legado de hijos y nietos; en ese mismo sentido también refirió un caso de éxito: el del Protocolo de Montreal, que logró la reducción del daño provocado a la capa de ozono a través de sustancias nocivas populares en aparatos de refrigeración, por otro lado, incitó a los asistentes a realizar pequeñas acciones que en lo individual pueden ayudar a una mejora global. 		
