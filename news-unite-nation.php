					<li>Reconocer el liderazgo</li>
					<li>Diálogo político</li>
					<li>Asociación de múltiples partes interesadas</li>
				</ol>
				<p class="paragraph-md text-blue_1 p-2">
					Los proyectos y actividades van alineadas a la “Agenda 2030”, en la cual se considera en el objetivo 16 “Paz, justicia e instituciones sólidas” que engloba temáticas relacionadas en aspectos preventivos de corrupción y áreas relacionadas.
					En próximas fechas se iniciará el desarrollo de proyecto entre United Nations Global Compact Network México y la SESEAY para un foro y talleres que enriquecerán y permitirá la participación de empresas, universidades y ciudadanía en general.
					Se realizan esfuerzos en conjunto, todo con el firme compromiso con la sociedad yucateca de fomentar una cultura de prevención, detección, control, disuasión y combate a la corrupción.
